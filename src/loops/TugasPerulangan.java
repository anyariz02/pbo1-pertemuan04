package loops;

public class TugasPerulangan {
    public static void main(String[] args) {
        int x;

        System.out.println("Perkalian 5-100 Menggunakan Perulangan For");

        for (x = 1; x<21; x++) {

            System.out.println("Perkalian " + x + " x 5" + " = " + x*5 );

        }
    }
}
